                <span>Принципы работы</span>
                <div class="hide">
                    <p>
                        В процессе обмена защищенными ЭПЦ документами генерируются открытый и закрытый ключи, представляющие собой коды различного объема. Один хранится на Вашем носителе информации, другой – передается вместе с документом. С подписанием в документ вносится информация с именем файла открытого ключа, Вашим именем и датой подписания. Адресат при получении Вашего документа использует открытый ключ и дешифрует текст, проверяя соответствие ЭЦП. Таким образом, подтверждается (или нет) подлинность документа.
                    </p>
