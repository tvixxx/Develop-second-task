            <div class="ecp-opisanie">
                <span>Виды ЭЦП</span>
                <div class="hide">
                    <p>
                        Существует три вида ЭЦП: простая, неквалифицированная усиленная и квалифицированная усиленная. Простая ЭЦП позволяет определить автора документа, но не дает возможности проверить документ на наличие в нем изменений с момента подписания. Неквалифицированная усиленная ЭЦП позволяет отслеживать изменения в документе. Создаваться неквалифицированная усиленная ЭПЦ может как при помощи сертификата удостоверяющего центра, так и без него. Третий вид – квалифицированная усиленная ЭЦП - обязательно должна иметь сертификат удостоверяющего центра.
                    </p>
