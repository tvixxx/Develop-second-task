                <div class="hide">
                    <p>
                        Для получения ЭЦП юридические и физические лица могут обратиться в удостоверяющие центры, работу которых регулируют уполномоченные федеральные органы. По окончании срока действия сертификата ЭЦП необходимо заново пройти процедуру ее получения. Однако обратившись не позднее чем за 20 дней до окончания срока действия ЭЦП в удостоверяющий центр, можно получить продление сертификата электронной подписи.
