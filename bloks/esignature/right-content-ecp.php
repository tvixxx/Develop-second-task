            <div class="ecp-opisanie">
                <span>Использование подписи</span>
                <div class="hide">
                    <p>
                        С помощью Вашего секретного ключа Вы можете подписывать документы путем криптографического преобразования, которое и является электронно-цифровой подписью. Пользователь, получающий документ с ЭЦП, имея открытый ключ цифровой подписи, выполняет обратное криптографическое преобразование. Это позволяет проверить ЭПЦ. В случае обнаружения ошибки система сообщит об этом электронным письмом. Носитель сертификатов ключей ЭЦП должен быть защищен паролем, чтобы им не смогли воспользоваться посторонние лица. Нельзя передавать носитель с ключами ЭПЦ посторонним лицам. При малейшем подозрении, что кто-то из них получил доступ к сертификату ЭЦП, следует немедленно сообщить об этом в удостоверяющий центр.
