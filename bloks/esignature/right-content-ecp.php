            <div class="ecp-opisanie">
                <span>Физические носители ЭЦП</span>
                <div class="hide">
                    <p>
                        Сертификаты ключа ЭЦП записываются  на носители ограниченного доступа – Rutoken и eToken. Rutoken – это электронный идентификатор в виде USB-брелока, служащий для авторизации пользователя ПК, защиты электронной переписки и хранения персональных данных. Etoken – специальное устройство высокой степени защиты, предназначенное для хранения секретной информации: ключей шифрования, цифровых сертификатов и др.
                    </p>
