        <h2 class="product_type_title">Что такое ЭЦП?</h2>
        <h3 class="product_h">Зачем нужна электронно-цифровая подпись</h3>
        <p>Интернет открыл огромные возможности для получения государственных и коммерческих услуг. Все деловые электронные операции требуют защиты от несанкционированного доступа. Именно этой цели и служит ЭПЦ – определенный набор символов, созданный при помощи специального программного обеспечения. Документ с такой подписью откроет лишь его адресат. Подделка невозможна: в случае попытки внести изменения в документ код ЭПЦ тут же аннулируется. ЭЦП обеспечивает конфиденциальность электронных документов, сохраняет их целостность и защищает от подделок.</p>
